    points = {3:(5,6,7), 4:(4,10), 5:(4,), 6:(4,), 7:(5,),
        8:(6,7), 9:(8,), 10:(4,8),11:(5,6,7)}
    start = ' ' * length
    if line_index in points:
        s = ''
        s += ' ' * points[line_index][0]
        for i in range(length):
            if i in points[line_index]:
                s += '*'
            else:
                s += ' '
    else:
        s = ' ' * length
    return start + s

